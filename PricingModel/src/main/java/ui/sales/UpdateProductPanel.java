    private void cmbSupplierActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbSupplierActionPerformed
        // TODO add your handling code here:
        
        
    String selectedSupplier = (String) cmbSupplier.getSelectedItem();
    if (selectedSupplier != null && !selectedSupplier.equals("Select Supplier")) {
        supplier = supplierDirectory.findSupplier(selectedSupplier); // Assign selected supplier
        if (supplier != null) {
            populateProductTable(); // Refresh the product table
        } else {
            JOptionPane.showMessageDialog(this, "Supplier not found.", "Error", JOptionPane.ERROR_MESSAGE);
            supplier = null; // Reset supplier if not found
        }
    } else {
        supplier = null; // Reset supplier if no valid selection
        DefaultTableModel model = (DefaultTableModel) tblProduct.getModel();
        model.setRowCount(0); // Clear the table if no supplier is selected
    }
    
    }//GEN-LAST:event_cmbSupplierActionPerformed

