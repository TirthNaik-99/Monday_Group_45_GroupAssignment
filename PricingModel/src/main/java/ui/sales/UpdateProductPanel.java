    private void btnViewProductActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewProductActionPerformed
        // TODO add your handling code here:
        
        int selectedRow = tblProduct.getSelectedRow();

    // Check if a row is selected
    if (selectedRow < 0) {
        JOptionPane.showMessageDialog(this, "Please select a product to view.", "Warning", JOptionPane.WARNING_MESSAGE);
        return;
    }

    // Get the product details from the table's selected row
    String productName = tblProduct.getValueAt(selectedRow, 0).toString();
    double floorPrice = Double.parseDouble(tblProduct.getValueAt(selectedRow, 1).toString());
    double ceilingPrice = Double.parseDouble(tblProduct.getValueAt(selectedRow, 2).toString());
    double targetPrice = Double.parseDouble(tblProduct.getValueAt(selectedRow, 3).toString());

    // Display the values in the text fields
    jTextField1.setText(productName);
    jTextField2.setText(String.valueOf(floorPrice));
    jTextField3.setText(String.valueOf(ceilingPrice));
    jTextField4.setText(String.valueOf(targetPrice));
    
    
    }//GEN-LAST:event_btnViewProductActionPerformed

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        
        
         int selectedRow = tblProduct.getSelectedRow();
    if (selectedRow < 0) {
        JOptionPane.showMessageDialog(this, "No product selected to update.", "Warning", JOptionPane.WARNING_MESSAGE);
        return;
    }

    String productName = jTextField1.getText();
    int floorPrice = (int) Double.parseDouble(jTextField2.getText());
    int ceilingPrice =(int) Double.parseDouble(jTextField3.getText());
    int targetPrice = (int)Double.parseDouble(jTextField4.getText());

    Product product = supplier.getProductCatalog().getProductList().get(selectedRow);
    product.setName(productName);
//    product.setFloorPrice(floorPrice);
//    product.setCeilingPrice(ceilingPrice);
    product.setTargetPrice((int) targetPrice);

    populateProductTable(); // Refresh table
    JOptionPane.showMessageDialog(this, "Product updated successfully.");
    }//GEN-LAST:event_btnSaveActionPerformed

